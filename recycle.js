

function hoverOut(){
    let img = document.getElementById("pic-hover");
    let txt = document.getElementById("txt-hover");
    
    if (img.style.opacity === "1"){
        img.style.boxShadow = "0.5";
        txt.style.textDecoration = "underline";
    }
    else {
        img.style.opacity = "1";
        txt.style.textDecoration = "none"; 
    }
}
function hover(){
    let img = document.getElementById("pic-hover");
    let txt = document.getElementById("txt-hover");
    
    if (img.style.opacity === "0"){
        img.style.opacity = "1";
        txt.style.textDecoration = "none"; 
        img.style.transition = "0.2s";
        txt.style.transition = "0.2s";
    }
    else {
        img.style.opacity = "0.5";
        txt.style.textDecoration = "underline";
        img.style.transition = "0.2s";
        txt.style.transition = "0.2s";
    }
}






